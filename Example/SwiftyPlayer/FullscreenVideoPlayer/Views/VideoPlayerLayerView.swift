import UIKit
import AVFoundation

final class VideoPlayerLayerView: UIView {
    
    deinit {
        playerLayer.player = nil
    }
    
    override class var layerClass: AnyClass {
        return AVPlayerLayer.self
    }
    
    var playerLayer: AVPlayerLayer {
        return layer as! AVPlayerLayer
    }
}
